package org.ehony.awt.font;

/**
 * Enumeration of styles of graphic elements that make up letters in a typeface.
 */
public enum Style
{
    /**
     * <a href="http://en.wikipedia.org/wiki/Serif"></a>
     */
    Roman,
    /**
     * <a href="http://en.wikipedia.org/wiki/Sans_serif"></a>
     */
    Grotesque,

    /**
     * <a href="http://en.wikipedia.org/wiki/Script_(typefaces)"></a>
     */
    Handwritten,

    Ornamental,
    Condensed,
    Proportional,
    /**
     * <a href="http://en.wikipedia.org/wiki/Dingbat"></a>
     */
    Symbol,

    /**
     * <a href="http://en.wikipedia.org/wiki/Monospaced_font"></a>
     */
    Monospaced,

    /**
     * <a href="http://en.wikipedia.org/wiki/Blackletter"></a>
     */
    Gothic
}
